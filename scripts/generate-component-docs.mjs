import { mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';

const cwd = process.cwd();
const outputDirectory = path.join(cwd, 'docs/components/reference');
const roots = [
  { directory: 'src/core/components', alias: '@core/components' },
  { directory: 'src/core/ui', alias: '@core/ui' },
  { directory: 'src/components', alias: '@/components' },
];

function titleCase(value) {
  return value
    .replace(/\.(tsx|ts)$/, '')
    .replace(/[-_]+/g, ' ')
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

async function walk(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = await Promise.all(
    entries.map((entry) => {
      const target = path.join(directory, entry.name);
      return entry.isDirectory() ? walk(target) : target;
    })
  );
  return files.flat();
}

function categoryFor(relativePath, alias) {
  if (relativePath.includes('/icons/') || relativePath.startsWith('icons/')) return 'icons';
  if (alias === '@core/ui') return 'ui-primitives';
  if (alias === '@/components') return 'starter-components';
  if (relativePath.startsWith('cards/')) return 'cards';
  if (relativePath.startsWith('charts/')) return 'charts';
  if (/^(controlled-table|table|table-utils)\//.test(relativePath) || relativePath === 'legacy-table.tsx') return 'tables';
  if (relativePath.startsWith('dnd/')) return 'drag-and-drop';
  if (relativePath.startsWith('google-map/')) return 'maps';
  if (relativePath.startsWith('invoice-builder/')) return 'invoice-builder';
  if (relativePath.startsWith('search/')) return 'search';
  if (/^(image-viewer|loader|banners)\//.test(relativePath)) return 'media-and-feedback';
  return 'other-components';
}

function exportedNames(source, filename) {
  const names = new Set();
  for (const match of source.matchAll(/export\s+(?:default\s+)?(?:async\s+)?(?:function|class|const|let|var)\s+([A-Za-z_$][\w$]*)/g)) {
    names.add(match[1]);
  }
  for (const match of source.matchAll(/export\s*\{([^}]+)\}/g)) {
    for (const item of match[1].split(',')) {
      const name = item.trim().split(/\s+as\s+/).at(-1)?.trim();
      if (name && /^[A-Za-z_$][\w$]*$/.test(name)) names.add(name);
    }
  }
  if (source.includes('export default') && names.size === 0) {
    names.add(titleCase(filename).replaceAll(' ', ''));
  }
  return [...names];
}

function propsNames(source) {
  return [...source.matchAll(/(?:interface|type)\s+([A-Za-z_$][\w$]*Props)\b/g)].map(
    (match) => match[1]
  );
}

function descriptionFor(relativePath) {
  const filename = path.basename(relativePath);
  const parent = path.basename(path.dirname(relativePath));
  const name = titleCase(filename);
  if (filename === 'index.tsx') return `${titleCase(parent)} component entry point.`;
  return `${name} component.`;
}

const entries = [];
for (const root of roots) {
  const absoluteRoot = path.join(cwd, root.directory);
  for (const file of await walk(absoluteRoot)) {
    if (!file.endsWith('.tsx')) continue;
    const relativePath = path.relative(absoluteRoot, file).split(path.sep).join('/');
    const source = await readFile(file, 'utf8');
    const importSuffix = relativePath.replace(/\.tsx$/, '').replace(/\/index$/, '');
    entries.push({
      category: categoryFor(relativePath, root.alias),
      description: descriptionFor(relativePath),
      exports: exportedNames(source, path.basename(file)),
      importPath: `${root.alias}/${importSuffix}`,
      props: propsNames(source),
      relativePath: path.relative(cwd, file).split(path.sep).join('/'),
      client: /^['\"]use client['\"];?/m.test(source),
    });
  }
}

entries.sort((a, b) => a.importPath.localeCompare(b.importPath));
await rm(outputDirectory, { recursive: true, force: true });
await mkdir(outputDirectory, { recursive: true });

const categories = new Map();
for (const entry of entries) {
  const categoryEntries = categories.get(entry.category) ?? [];
  categoryEntries.push(entry);
  categories.set(entry.category, categoryEntries);
}
const indexRows = [];
for (const [category, categoryEntries] of [...categories].sort(([a], [b]) => a.localeCompare(b))) {
  const title = titleCase(category);
  const lines = [
    `# ${title}`,
    '',
    `> Generated by \`pnpm docs:components\`. Contains ${categoryEntries.length} component modules.`,
    '',
    '| Component exports | Import path | Props types | Rendering | Source |',
    '| --- | --- | --- | --- | --- |',
  ];
  for (const entry of categoryEntries) {
    const names = entry.exports.length ? entry.exports.map((name) => `\`${name}\``).join(', ') : 'module exports';
    const props = entry.props.length ? entry.props.map((name) => `\`${name}\``).join(', ') : '—';
    lines.push(`| ${names} | \`${entry.importPath}\` | ${props} | ${entry.client ? 'Client' : 'Universal/server-safe'} | [file](../../../${entry.relativePath}) |`);
  }
  lines.push('');
  await writeFile(path.join(outputDirectory, `${category}.md`), lines.join('\n'));
  indexRows.push(`| [${title}](./reference/${category}.md) | ${categoryEntries.length} |`);
}

const supportGroups = [
  ['Hooks', 'src/core/hooks', '@core/hooks'],
  ['Providers', 'src/core/providers', '@core/providers'],
  ['Types', 'src/core/types', '@core/types'],
  ['Utilities', 'src/core/utils', '@core/utils'],
];
const supportLines = ['# Supporting APIs', '', 'Use these modules when a component needs state, shared types, or helpers.', ''];
for (const [title, directory, alias] of supportGroups) {
  const files = (await walk(path.join(cwd, directory)))
    .filter((file) => /\.(ts|tsx)$/.test(file))
    .sort();
  supportLines.push(`## ${title}`, '', '| Import path | Source |', '| --- | --- |');
  for (const file of files) {
    const relative = path.relative(path.join(cwd, directory), file).split(path.sep).join('/');
    const suffix = relative.replace(/\.(ts|tsx)$/, '').replace(/\/index$/, '');
    const source = path.relative(cwd, file).split(path.sep).join('/');
    supportLines.push(`| \`${alias}/${suffix}\` | [file](../../${source}) |`);
  }
  supportLines.push('');
}
await writeFile(path.join(cwd, 'docs/components/SUPPORTING_APIS.md'), supportLines.join('\n'));

const index = [
  '# Component Catalog',
  '',
  '> AI entry point: use this file to choose a category, then open the linked reference and source file.',
  '',
  `The repository contains **${entries.length} component modules** ported from the Isomorphic template and retained starter components.`,
  '',
  '| Category | Modules |',
  '| --- | ---: |',
  ...indexRows,
  '',
  'See [Supporting APIs](./SUPPORTING_APIS.md) for hooks, providers, shared types, and utilities.',
  '',
];
await writeFile(path.join(cwd, 'docs/components/INDEX.md'), index.join('\n'));

console.log(`Documented ${entries.length} component modules in ${categories.size} categories.`);
